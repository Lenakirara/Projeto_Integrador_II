	private void get_questionario_info(){
                int row = jTable_Questionario.getSelectedRow();
                int col = jTable_Questionario.getSelectedColumn();	
                
		Object data_id = (Object)jTable_Questionario.getValueAt(row, 0);
                
                if (data_id != null){
		   this.questionario_id =  Integer.parseInt(String.valueOf(data_id));
                }
		System.out.println("questionario_id: " + this.questionario_id);
		Object data_descricao = (Object)jTable_Questionario.getValueAt(row, 1);
                if (data_descricao != null){
		   this.descricao =  String.valueOf(data_descricao);
                }				
	}
