        private void jButton_GraficoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_GraficoActionPerformed
		if (this.questionario_id <= 0) {
			JOptionPane.showMessageDialog(this, "Selecione um questionário para geração do gráfico.");
			this.jTable_Questionario.requestFocus();
			
		} else {
			Frm_Questionario_Grafico chart = new Frm_Questionario_Grafico("Estatistica", "Questionário: " + this.descricao, this.questionario_id);
			chart.questionario_id = this.questionario_id;
			chart.pack();
			RefineryUtilities.centerFrameOnScreen(chart);
			chart.setVisible(true);
		}
        }//GEN-LAST:event_jButton_GraficoActionPerformed

